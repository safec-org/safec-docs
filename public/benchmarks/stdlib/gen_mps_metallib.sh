#!/bin/sh
# Compiles gpu_mps_kernels.metal to a .metallib and regenerates
# gpu_mps_metallib.h (the embedded byte array gpu_mps.sc loads at runtime).
# Run this after editing gpu_mps_kernels.metal; the generated header is
# checked in, so this only needs to run when the kernel source changes, not
# as part of every SafeC build.
set -e
cd "$(dirname "$0")"

xcrun -sdk macosx metal -c gpu_mps_kernels.metal -o /tmp/gpu_mps_kernels.air
xcrun -sdk macosx metallib /tmp/gpu_mps_kernels.air -o /tmp/gpu_mps_kernels.metallib
SIZE=$(wc -c < /tmp/gpu_mps_kernels.metallib | tr -d ' ')

{
    echo "// Generated by gen_mps_metallib.sh from gpu_mps_kernels.metal — do not"
    echo "// edit directly. Regenerate after changing the kernel source."
    echo "#pragma once"
    echo ""
    echo "namespace std {"
    echo ""
    printf 'static const unsigned char __mps_metallib_bytes[%s] = {' "$SIZE"
    od -An -v -tu1 /tmp/gpu_mps_kernels.metallib | tr -s ' ' '\n' | grep -v '^$' | awk '{printf "%s,", $1}' | sed 's/,$//'
    printf '};\n'
    echo "static const unsigned long __mps_metallib_size = ${SIZE}UL;"
    echo ""
    echo "} // namespace std"
} > gpu_mps_metallib.h

rm -f /tmp/gpu_mps_kernels.air /tmp/gpu_mps_kernels.metallib
echo "wrote gpu_mps_metallib.h ($(wc -c < gpu_mps_metallib.h) bytes, metallib $SIZE bytes)"
